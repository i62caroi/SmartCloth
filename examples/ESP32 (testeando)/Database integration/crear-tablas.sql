CREATE TABLE comida (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    fecha VARCHAR(12) NOT NULL,
    hora VARCHAR(12) NOT NULL
);


--------------------------------------------------
--------------------------------------------------

CREATE TABLE plato (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    comida_id INT NOT NULL,
    FOREIGN KEY (comida_id) REFERENCES comida(ID)
);

