CREATE TABLE alimento (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    grupo INT NOT NULL,
    peso FLOAT NOT NULL,
    plato_id INT NOT NULL,
    FOREIGN KEY (plato_id) REFERENCES plato(ID)
);

